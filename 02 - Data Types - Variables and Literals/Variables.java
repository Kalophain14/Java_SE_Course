/* What are variables?
 */

public class Variables
{
    public static void main(String [] args)
    {
        byte b = 2;
        int a = 1000;
        float f = 23.5f;
        char c = 'A';

        System.out.println(b);
        System.out.println(a);
        System.out.println(f);
        System.out.println(c);
    }
}