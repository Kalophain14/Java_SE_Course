public class CheckSizeRange{
    public static void main(String[] args)
    {
        System.out.println("Sizes of Integer :");
        System.out.println("Int Min: "+ Integer.MIN_VALUE);
        System.out.println("Int Max: "+ Integer.MAX_VALUE);
        System.out.println("Int Bytes: "+ Integer.BYTES);

        System.out.println("Sizes of Float :");
        System.out.println("Int Min: "+ Float.MIN_VALUE);
        System.out.println("Int Max: "+ Float.MAX_VALUE);
        System.out.println("Int Bytes: "+ Float.BYTES);

        System.out.println("Sizes of Byte :");
        System.out.println("Int Min: "+ Byte.MIN_VALUE);
        System.out.println("Int Max: "+ Byte.MAX_VALUE);
        System.out.println("Int Bytes: "+ Byte.BYTES);

        System.out.println("Sizes of Long :");
        System.out.println("Int Min: "+ Long.MIN_VALUE);
        System.out.println("Int Max: "+ Long.MAX_VALUE);
        System.out.println("Int Bytes: "+ Long.BYTES);

        System.out.println("Sizes of Double :");
        System.out.println("Int Min: "+ Double.MIN_VALUE);
        System.out.println("Int Max: "+ Double.MAX_VALUE);
        System.out.println("Int Bytes: "+ Double.BYTES);
    }
}